// --- API Route Handler ---

export async function POST(req: Request) {
  try {
    const { name, email, message } = await req.json();

    // 1. Validate Input (Basic Check)
    if (!name || !email || !message) {
      return NextResponse.json(
        {
          success: false,
          message:
            "‚ùå Please fill out all required fields (Name, Email, Message).",
        },
        { status: 400 }
      );
    }

    // 2. Setup Transporter
    const transporter = nodemailer.createTransport({
      host: "mail.bornosoftnr.com",
      port: 465,
      secure: true,
      auth: {
        user: "info@bornosoftnr.com",
        pass: "2FZY{jx24)T$KzLm", // Replace with secure environment variable
      },
    });

    // 3. Generate HTML Content
    const adminHtml = getAdminEmailHtml(name, email, message);
    const clientHtml = getClientConfirmationHtml(name, message);

    // 4. Send Emails Concurrently
    await Promise.all([
      // A. Email to Bornosoft Team (Admin)
      transporter.sendMail({
        from: `"${name} (Web Inquiry)" <info@bornosoftnr.com>`, // Display client's name
        replyTo: email, // Reply goes back to the client
        to: "info@bornosoftnr.com",
        subject: `[New Inquiry] Contact Form Submission from ${name}`,
        html: adminHtml,
      }),

      // B. Email to Client (Confirmation)
      transporter.sendMail({
        from: '"Bornosoft NR Team" <no-reply@bornosoftnr.com>',
        replyTo: "info@bornosoftnr.com", // Client replies to the main info mailbox
        to: email,
        subject: `Confirmation: We've Received Your Message - Bornosoft NR`,
        html: clientHtml,
      }),
    ]);

    // 5. Success Response
    return NextResponse.json({
      success: true,
      message:
        "‚úÖ Your message was sent successfully. A confirmation email has been sent to your inbox.",
    });
  } catch (error) {
    // 6. Error Handling
    console.error("Email sending error:", error);
    return NextResponse.json(
      {
        success: false,
        message:
          "‚ùå Failed to send email due to a server error. Please check the console.",
      },
      { status: 500 }
    );
  }
}
